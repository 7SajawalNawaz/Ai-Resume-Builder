import React, { useEffect, useState } from 'react'
import { useParams } from 'react-router-dom'
// import { dummyResumeData } from '../assets/assets'
import Loader from '../components/Loader'
import ResumePreview from '../components/FormResume/ResumePreview'
import { ArrowLeftIcon, SendToBack } from 'lucide-react'
import api from '../config/api'

const Preview = () => {
  const {resumeId} = useParams()

  const [resumeData , setResumeData] = useState(null)
  const [isLoading , setisLoading] = useState(true)

  const loadResume = async () =>{
    try{
      const {data} = await api.get('/api/resumes/public/' + resumeId )
      setResumeData(data.resumes)
    }catch(err){
      console.log(err.message);
    } finally {
      setisLoading(false)
    }
  }

  useEffect(()=>{
    loadResume()
  },[])

  return resumeData ? (
    <div className='bg-slate-100'>
      <div className='max-w-3xl mx-auto py-10'>
        <ResumePreview data={resumeData} 
        template={resumeData.template} 
        accentColor={resumeData.accent_color}
        classes='py-4 '/>

      </div>
    </div>
  ) : (
    <div>
    {
      isLoading ? <Loader /> :(
        <div className='flex flex-col items-center justify-center h-screen'>
          <p className='text-center text-6xl text-slate-400 font-medium'>Resume not found</p>
          <a className='mt-6 bg-custompurple hover:bg-purple-600
          text-white rounded-full px-6 h-9 m-1 ring-offset-1 ring-1 
          ring-purple-400  flex items-center transition-colors' href="/">
            <ArrowLeftIcon className='mr-2 size-4' />
            Go to home page
          </a>
        </div>
      )
    }
    </div>
  )
}

export default Preview